package ah;

public class Docter {
	
	private String name;
	private String gender;
	private String perphno;
	private String contactphno;
	private String dati;
	private String speciality;
	private String almamatter;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getPerphno() {
		return perphno;
	}
	public void setPerphno(String perphno) {
		this.perphno = perphno;
	}
	public String getContactphno() {
		return contactphno;
	}
	public void setContactphno(String contactphno) {
		this.contactphno = contactphno;
	}
	public String getDati() {
		return dati;
	}
	public void setDati(String dati) {
		this.dati = dati;
	}
	public String getSpeciality() {
		return speciality;
	}
	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}
	public String getAlmamatter() {
		return almamatter;
	}
	public void setAlmamatter(String almamatter) {
		this.almamatter = almamatter;
	}
	
	
}
